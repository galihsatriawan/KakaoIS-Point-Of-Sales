    private void tampilkan_laporan() {

        //Copy model laporan terlebih dahulu
        DefaultTableModel dtm = (DefaultTableModel) tblLaporan.getModel();
        //Hapus data awalnya
        dtm.getDataVector().removeAllElements();

        String sql;
        sql = "SELECT\n"
                + "	nt.idNota,nt.hargaJual,nt.diskon,nt.total,nt.tunai,nt.tglWaktu,nt.namaPemesan,us.username\n"
                + "FROM\n"
                + "	tb_nota nt\n"
                + "JOIN tb_user us ON us.idUser = nt.idUser\n"
                + "AND DATE(nt.tglWaktu) BETWEEN '" + startDateL + "'AND '" + endDateL + "'\n"
                + "ORDER BY tglWaktu DESC";
        ResultSet hasil = Database.query(sql);
        System.out.println(sql);
        int i = 0, totalPendapatan = 0;
        try {
            //Jika kosong langsung tangkap, dimana hasil.next() == ada
            if (!hasil.next()) {
                String kolom[] = {"", "", "", "", "", "", ""};
                dtm.addRow(kolom);
                txtTotalPendapatan.setText(totalPendapatan + "");
                tblLaporan.setModel(dtm);
                return ;
            } else {
                hasil.previous();
                while (hasil.next()) {
                    //dapatkan data querynya
                    System.out.println("hahah");
                    int idNota = hasil.getInt("idNota");
                    int hargaJual = hasil.getInt("hargaJual");
                    int diskon = hasil.getInt("diskon");
                    int total = hasil.getInt("total");
                    //int tunai = hasil.getInt("tunai");
                    String datetime = hasil.getString("tglWaktu");
                    String pemesan = hasil.getString("namaPemesan");
                    String user = hasil.getString("username");
                    totalPendapatan += total;

                    String kolom[] = {(++i) + "", idNota + "", datetime, "" + hargaJual, "" + total, pemesan, user};
                    dtm.addRow(kolom);

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(LaporanFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        //copykan dtm ke table
        txtTotalPendapatan.setText(totalPendapatan + "");
        tblLaporan.setModel(dtm);
    }

    private void tampilkan_produk_terbaik() {
        DefaultTableModel dtm = (DefaultTableModel) tblProdukTerbaik.getModel();
        dtm.getDataVector().removeAllElements();

        String sql;
        sql = "SELECT\n"
                + "	p.idProduk,p.namaProduk, SUM(banyak) terjual\n"
                + "FROM\n"
                + "	tb_nota n,tb_dt_nota dn,tb_produk p\n"
                + "WHERE\n"
                + "	n.idNota = dn.idNota AND p.idProduk = dn.idProduk AND DATE(n.tglWaktu) BETWEEN '" + startDateP + "'\n"
                + "	AND '" + endDateP + "' " + jenisProduk + " \n"
                + "GROUP BY p.idProduk\n"
                + "ORDER BY terjual DESC LIMIT 10;";
        ResultSet result = Database.query(sql);
        System.out.println(sql);
        int i = 0;
        try {
            
            if (!result.next()) {
                String kolom[] = {"", "", "", "", "", "", ""};
                dtm.addRow(kolom);
                tblProdukTerbaik.setModel(dtm);
                System.out.println("yah");
                return ;
            } else {
                result.previous();
                while (result.next()) {
                    System.out.println("hahah");
                    int idProduk = result.getInt("idProduk");
                    String nama = result.getString("namaProduk");
                    int terjual = result.getInt("terjual");
                    String kolom[] = {(++i) + "", "" + idProduk, nama, "" + terjual};
                    dtm.addRow(kolom);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(LaporanFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblProdukTerbaik.setModel(dtm);
    }
