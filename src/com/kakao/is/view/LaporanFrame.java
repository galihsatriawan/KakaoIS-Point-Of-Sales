    private void btnDetailLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailLaporanActionPerformed
        // TODO add your handling code here:
        int posisi = -1;
        posisi = tblLaporan.getSelectedRow();
        if (posisi != -1) {
            int idNota = Integer.parseInt(tblLaporan.getValueAt(posisi, 1).toString());
            String sql = "CALL receipt(" + idNota + ")";
            System.out.println(sql);
            /*
            Langkah -langkah
            1. Dapatkan seluruh data receipt dari database
            2. pindahkan seluruh data ke array list
            3. dapatkan keterangan2 nya juga
             */
            keranjang.clear();
            ResultSet hasil = Database.query(sql);
            try {
                //dapatkan keterangan static seperti kasir,pembeli,tgl ,diskon,tunai
                hasil.next();
                String datetime = hasil.getDate("tglWaktu").toString();
                String kasir = hasil.getString("username");
                String pembeli = hasil.getString("namaPemesan");
                int diskon = hasil.getInt("diskon");
                int tunai = hasil.getInt("tunai");
                int hargaJual = 0;
                int total = 0;
                int kembali = 0;
                int idProduk;
                String nama;
                int hargaProduk;
                int qty;
                int subTotal;

                do {
                    //pindah ke array list
                    idProduk = hasil.getInt("idProduk");
                    nama = hasil.getString("namaProduk");
                    hargaProduk = hasil.getInt("hargaJual"); //dapat dari produk
                    qty = hasil.getInt("banyak");
                    subTotal = hasil.getInt("subtotal"); //atau manual hargaProduk * qty
                    hargaJual += subTotal;
                    MenuTrolly tmb_barang = new MenuTrolly(idProduk, nama, hargaProduk, qty, subTotal);
                    keranjang.add(tmb_barang);
                } while (hasil.next());

                total = hargaJual - diskon;
                kembali = tunai - total;
                
                
                ReceiptDetailFrame.instance = null;
                ReceiptDetailFrame.MODE = 2;
                ReceiptDetailFrame.set_keterangan(idNota, datetime, kasir, pembeli, hargaJual,
                        diskon, total, tunai, kembali, keranjang);
                ReceiptDetailFrame receipt = ReceiptDetailFrame.getInstance();
               
                receipt.setVisible(true);
                receipt.setLocationRelativeTo(null);
            } catch (SQLException ex) {
                Logger.getLogger(LaporanFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {

            JOptionPane.showMessageDialog(this, "Silahkan pilih produk terlebih dahulu",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            this.setVisible(true);
        
        }
    }//GEN-LAST:event_btnDetailLaporanActionPerformed

