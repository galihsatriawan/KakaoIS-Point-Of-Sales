    public void tampilkan_staff() {
        DefaultTableModel dtm = (DefaultTableModel) tblStaff.getModel();
        dtm.getDataVector().removeAllElements();
        int i = 0;
        for (i = 0; i < arrayStaff.size(); i++) {
            Staff cur = arrayStaff.get(i);

            String username = cur.getUsername();
            String nama = cur.getNamaStaff();
            String jabatan = cur.getJabatan();
            String jk = cur.getJk() == 'L' ? "Laki-Laki" : "Perempuan";
            String alamat = cur.getAlamat();
            String telp = cur.getTelp();
            String kolom[] = {(i + 1) + "", username, nama,jabatan, jk, alamat, telp};
            dtm.addRow(kolom);
        }
        tblStaff.setModel(dtm);

    }

