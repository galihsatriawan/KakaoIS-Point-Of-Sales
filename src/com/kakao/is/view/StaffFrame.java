    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int posisi = -1;
        posisi = tblStaff.getSelectedRow();
        if (posisi != -1) {
            String username = tblStaff.getValueAt(posisi, 1).toString();

            Staff hapusStaff = listStaff.get(username);
            int idStaff = hapusStaff.getIdStaff();
            int idUser = hapusStaff.getIdUser();

            //arrayStaff.remove(hapusStaff); //doesn't work properly
            for (int i = 0; i < arrayStaff.size(); i++) {
                if (arrayStaff.get(i) == hapusStaff) {
                    arrayStaff.remove(i);
                    break;
                }
            }
            listStaff.remove(username);
            //hapus dari database
            //pertama hapus dari tb_dt_user terlebih dahulu

            String sql = "DELETE FROM tb_dt_user WHERE idStaff = " + idStaff;
            Database.update(sql);
            //Hapus dari tb_user
            sql = "DELETE FROM tb_user WHERE idUser = " + idUser;
            Database.update(sql);
            //Hapus dari tb_staff
            sql = "DELETE FROM tb_staff WHERE idStaff = " + idStaff;
            Database.update(sql);

            tampilkan_staff();
        } else {
            JOptionPane.showMessageDialog(this, "Silahkan pilih Staff terlebih dahulu", "Staff", JOptionPane.WARNING_MESSAGE);
            this.setVisible(true);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

